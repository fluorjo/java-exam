public class gns1switch {
    public static void main(String[]args) {
        char bt;
        bt='A';
        switch (bt) {
            case 'A':
            System.out.println("AA");
            break;
            case 'B':
            System.out.println("BB");
            break;
            case 'C':
            System.out.println("CC");
            break;

            default:
            System.out.println("xxxxx");
        }
        }
    }

